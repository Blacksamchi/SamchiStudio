#include <studio.h>
int main(void)
{
  int x;
  printf("정수를 입력라아아아\n"); scanf("%d",&x);
  printf("정수에 10을 더한 값은 %d이다아아.\n",x+10);
  printf("정수에 10을 뺀 값은 %d이올시다.\n",x-10);
  printf("정수에 10을 곱한 값은 %d다제\n",x*10);
  printf("정수에 10으로 나눈 몫은 %d라네\n",x/10);
  printf("정수에 10으로 나눈 나머지는 %d라오\n",x%10);
  return(0);
}
